"""Generate LaTeX tables and macros for the INFOCOM paper from canonical JSON."""

from __future__ import annotations

import argparse
import json
from pathlib import Path
import sys


REPO_ROOT = Path(__file__).resolve().parents[1]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from benchmarks.generate_infocom_figures import gate_d_rows, short_model_name


RAW = "async_raw"
CUSZP = "async_cuszp_1e-5"
METHOD_LABELS = {
    RAW: "Raw",
    CUSZP: r"cuSZp $10^{-5}$",
    "async_zstd": "Zstd",
    "async_lz4": "LZ4",
    "async_int8": "INT8",
    "async_adaptive": "Adaptive mixed",
}

PAPER_SOURCE = REPO_ROOT / "paper/conference_101719.tex"


def generated_block(name: str, text: str) -> str:
    return (
        f"% BEGIN GENERATED: {name}\n"
        "% Generated from canonical experiment JSON; do not edit this block.\n"
        f"{text.rstrip()}\n"
        f"% END GENERATED: {name}"
    )


def sync_paper_blocks(
    paper_path: Path, assets: dict[str, str], *, check_only: bool = False
) -> bool:
    """Update or verify generated blocks in the standalone Overleaf source."""
    source = paper_path.read_text(encoding="utf-8")
    updated = source
    for name, text in assets.items():
        begin = f"% BEGIN GENERATED: {name}"
        end = f"% END GENERATED: {name}"
        if updated.count(begin) != 1 or updated.count(end) != 1:
            raise ValueError(
                f"paper must contain exactly one generated block named {name}"
            )
        start = updated.index(begin)
        finish = updated.index(end, start) + len(end)
        updated = updated[:start] + generated_block(name, text) + updated[finish:]
    if check_only:
        if updated != source:
            raise ValueError(
                f"{paper_path} contains stale generated result blocks"
            )
        return False
    if updated != source:
        paper_path.write_text(updated, encoding="utf-8")
        return True
    return False


def load(path: Path) -> dict:
    with path.open(encoding="utf-8") as stream:
        return json.load(stream)


def write(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(
        "% Generated by benchmarks/generate_paper_assets.py; do not edit.\n"
        + text.rstrip()
        + "\n",
        encoding="utf-8",
    )


def pm(metric: dict, digits: int = 3) -> str:
    return (
        "$"
        + f"{metric['mean']:.{digits}f}"
        + f"\\pm{metric['ci95_half_width']:.{digits}f}$"
    )


def quality_word(value: bool | None) -> str:
    if value is True:
        return "Pass"
    if value is False:
        return "Reject"
    return "--"


def concurrency_table(document: dict) -> str:
    lines = [
        r"\begin{table*}[t]",
        r"\centering",
        r"\caption{Qwen2.5-1.5B simultaneous-burst results (five interleaved trials). Differences are cuSZp minus raw; negative latency is better.}",
        r"\label{tab:concurrency}",
        r"\scriptsize",
        r"\setlength{\tabcolsep}{4pt}",
        r"\begin{tabular}{r r r r r r}",
        r"\toprule",
        r"Conc. & Raw handler & cuSZp handler & Paired handler $\Delta$ & Initial req/s (raw $\rightarrow$ cuSZp) & Initial E2E $\Delta$ \\",
        r"\midrule",
    ]
    for level in document["levels"]:
        if level.get("status") != "complete":
            continue
        result = level["result"]
        raw = result["methods"][RAW]
        cu = result["methods"][CUSZP]
        lines.append(
            "{} & {:.3f} ms & {:.3f} ms & {} ms & {:.3f} $\\rightarrow$ {:.3f} & {} ms \\\\".format(
                level["concurrency"],
                raw["total_handler_ms"],
                cu["total_handler_ms"],
                pm(cu["total_handler_vs_raw_ms"]),
                raw["initial_requests_per_second"],
                cu["initial_requests_per_second"],
                pm(cu["paired_vs_raw"]["initial_e2e_ms"]),
            )
        )
    lines.extend([r"\bottomrule", r"\end{tabular}", r"\end{table*}"])
    return "\n".join(lines)


def open_loop_table(documents: list[dict]) -> str:
    rows = []
    for document in documents:
        for level in document["levels"]:
            if level.get("status") != "complete":
                continue
            result = level["result"]
            raw = result["methods"][RAW]
            cu = result["methods"][CUSZP]
            rows.append((document["model"], result, raw, cu))
    rows.sort(key=lambda item: (item[0], item[1]["offered_requests_per_second"]))
    lines = [
        r"\begin{table*}[t]",
        r"\centering",
        r"\caption{Fixed-rate open-loop results. P95 values pool all requests across five trials; the final column is a paired trial-level confidence interval.}",
        r"\label{tab:openloop}",
        r"\scriptsize",
        r"\setlength{\tabcolsep}{3.5pt}",
        r"\begin{tabular}{l r r r r r r}",
        r"\toprule",
        r"Model & Offered & Achieved (raw $\rightarrow$ cuSZp) & TTFT P95 (raw $\rightarrow$ cuSZp) & E2E P95 (raw $\rightarrow$ cuSZp) & Handler $\Delta$ & Mean E2E $\Delta$ \\",
        r"\midrule",
    ]
    for model, result, raw, cu in rows:
        lines.append(
            "{} & {:.0f} & {:.3f} $\\rightarrow$ {:.3f} & {:.1f} $\\rightarrow$ {:.1f} ms & {:.1f} $\\rightarrow$ {:.1f} ms & {} ms & {} ms \\\\".format(
                short_model_name(model),
                result["offered_requests_per_second"],
                raw["achieved_requests_per_second"],
                cu["achieved_requests_per_second"],
                raw["latency"]["ttft_ms"]["p95"],
                cu["latency"]["ttft_ms"]["p95"],
                raw["latency"]["e2e_ms"]["p95"],
                cu["latency"]["e2e_ms"]["p95"],
                pm(cu["total_handler_vs_raw_ms"]),
                pm(cu["paired_vs_raw"]["initial_e2e_ms"]),
            )
        )
    lines.extend([r"\bottomrule", r"\end{tabular}", r"\end{table*}"])
    return "\n".join(lines)


def gate_d_table(summary: dict) -> str:
    rows = gate_d_rows(summary)
    by_model = {row["model"]: row for row in rows}
    lines = [
        r"\begin{table*}[t]",
        r"\centering",
        r"\caption{Gate D cross-model fixed-bound comparison (five interleaved trials). Handler is G2C+C2G.}",
        r"\label{tab:gated}",
        r"\scriptsize",
        r"\setlength{\tabcolsep}{4pt}",
        r"\begin{tabular}{l r r r r c}",
        r"\toprule",
        r"Model & Ratio & Raw handler & cuSZp handler & Paired handler $\Delta$ & cuSZp quality \\",
        r"\midrule",
    ]
    for model in summary["models"]:
        if model.get("status") != "complete":
            continue
        methods = model["result"]["methods"]
        raw = methods[RAW]
        cu = methods[CUSZP]
        paired = by_model[model["model"]]
        lines.append(
            "{} & {:.3f}$\\times$ & {:.3f} ms & {:.3f} ms & $ {:.3f}\\pm{:.3f}$ ms & {} \\\\".format(
                short_model_name(model["model"]),
                cu["compression_ratio"],
                raw["gpu_to_cpu_ms"] + raw["cpu_to_gpu_ms"],
                cu["gpu_to_cpu_ms"] + cu["cpu_to_gpu_ms"],
                paired["handler_diff_ms"],
                paired["handler_ci95_ms"],
                quality_word(cu["quality_passed"]),
            )
        )
    lines.extend([r"\bottomrule", r"\end{tabular}", r"\end{table*}"])
    return "\n".join(lines)


def quality_table(document: dict) -> str:
    order = [RAW, CUSZP, "async_zstd", "async_lz4", "async_int8"]
    lines = [
        r"\begin{table}[t]",
        r"\centering",
        r"\caption{Qwen2.5-1.5B long-context needle-retrieval suite (five trials).}",
        r"\label{tab:quality}",
        r"\scriptsize",
        r"\setlength{\tabcolsep}{2.8pt}",
        r"\begin{tabular}{l r r r r c}",
        r"\toprule",
        r"Method & Ratio & Handler & Initial E2E & Task acc. & Gate \\",
        r"\midrule",
    ]
    for method in order:
        metrics = document["methods"][method]
        task = metrics["replay_task_accuracy"]["mean"]
        passed = document["quality_gate"]["methods"][method]["passed"]
        lines.append(
            "{} & {:.3f}$\\times$ & {:.1f} ms & {:.1f} ms & {:.1f}\\% & {} \\\\".format(
                METHOD_LABELS[method],
                metrics["compression_ratio"]["mean"],
                metrics["gpu_to_cpu_ms"]["mean"]
                + metrics["cpu_to_gpu_ms"]["mean"],
                metrics["initial_e2e_ms"]["mean"],
                100.0 * task,
                quality_word(passed),
            )
        )
    lines.extend([r"\bottomrule", r"\end{tabular}", r"\end{table}"])
    return "\n".join(lines)


def adaptive_table(document: dict) -> str:
    order = [RAW, CUSZP, "async_adaptive"]
    lines = [
        r"\begin{table}[t]",
        r"\centering",
        r"\caption{Adaptive mechanism ablation (two-trial pilot; not a formal performance claim).}",
        r"\label{tab:adaptive}",
        r"\scriptsize",
        r"\setlength{\tabcolsep}{3pt}",
        r"\begin{tabular}{l r r r c}",
        r"\toprule",
        r"Method & Ratio & G2C & C2G & Token/exact \\",
        r"\midrule",
    ]
    for method in order:
        metrics = document["methods"][method]
        lines.append(
            "{} & {:.3f}$\\times$ & {:.3f} ms & {:.3f} ms & {:.3f}/{:.3f} \\\\".format(
                METHOD_LABELS[method],
                metrics["compression_ratio"]["mean"],
                metrics["gpu_to_cpu_ms"]["mean"],
                metrics["cpu_to_gpu_ms"]["mean"],
                metrics["token_match_rate"]["mean"],
                metrics["exact_match_rate"]["mean"],
            )
        )
    lines.extend([r"\bottomrule", r"\end{tabular}", r"\end{table}"])
    return "\n".join(lines)


def result_macros(concurrency: dict, qwen1p5: dict, qwen0p5: dict) -> str:
    c8 = next(level for level in concurrency["levels"] if level["concurrency"] == 8)
    raw_c8 = c8["result"]["methods"][RAW]
    cu_c8 = c8["result"]["methods"][CUSZP]
    r6 = next(
        level
        for level in qwen1p5["levels"]
        if level["result"]["offered_requests_per_second"] == 6.0
    )
    raw_r6 = r6["result"]["methods"][RAW]
    cu_r6 = r6["result"]["methods"][CUSZP]
    r8_small = qwen0p5["levels"][0]["result"]
    raw_small = r8_small["methods"][RAW]
    cu_small = r8_small["methods"][CUSZP]
    definitions = {
        "ConcurrencyHandlerReductionPct": 100
        * (raw_c8["total_handler_ms"] - cu_c8["total_handler_ms"])
        / raw_c8["total_handler_ms"],
        "ConcurrencyThroughputGainPct": 100
        * (
            cu_c8["initial_requests_per_second"]
            / raw_c8["initial_requests_per_second"]
            - 1
        ),
        "ArrivalAchievedGainPct": 100
        * (
            cu_r6["achieved_requests_per_second"]
            / raw_r6["achieved_requests_per_second"]
            - 1
        ),
        "ArrivalTTFTPReductionPct": 100
        * (
            raw_r6["latency"]["ttft_ms"]["p95"]
            - cu_r6["latency"]["ttft_ms"]["p95"]
        )
        / raw_r6["latency"]["ttft_ms"]["p95"],
        "SmallHandlerReductionPct": 100
        * (raw_small["total_handler_ms"] - cu_small["total_handler_ms"])
        / raw_small["total_handler_ms"],
    }
    return "\n".join(
        rf"\newcommand{{\{name}}}{{{value:.1f}\%}}"
        for name, value in definitions.items()
    )


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--out-dir", type=Path, default=REPO_ROOT / "paper/generated")
    parser.add_argument("--data-root", type=Path, default=REPO_ROOT / "data")
    parser.add_argument("--paper", type=Path, default=PAPER_SOURCE)
    paper_mode = parser.add_mutually_exclusive_group()
    paper_mode.add_argument("--sync-paper", action="store_true")
    paper_mode.add_argument("--check-paper", action="store_true")
    args = parser.parse_args()

    data_root = args.data_root.resolve()
    concurrency = load(data_root / "qwen1.5b_real_concurrency_v3/concurrency_summary.json")
    qwen1p5 = load(data_root / "qwen1.5b_open_loop_arrival_v1/arrival_rate_summary.json")
    qwen0p5 = load(data_root / "qwen0.5b_open_loop_arrival_v1/arrival_rate_summary.json")
    gate_d = load(data_root / "gate_d_fair_async_probe/gate_d_summary.json")
    quality = load(data_root / "infocom_long_context_quality_v1/five_trial/aggregate.json")
    adaptive = load(data_root / "vllm_qwen1.5b_4k_gate_c_packed_mixed21_complete_2trial/aggregate.json")

    assets = {
        "results_macros": result_macros(concurrency, qwen1p5, qwen0p5),
        "table_concurrency": concurrency_table(concurrency),
        "table_open_loop": open_loop_table([qwen0p5, qwen1p5]),
        "table_gate_d": gate_d_table(gate_d),
        "table_quality": quality_table(quality),
        "table_adaptive": adaptive_table(adaptive),
    }
    for name, text in assets.items():
        write(args.out_dir / f"{name}.tex", text)
    if args.sync_paper or args.check_paper:
        changed = sync_paper_blocks(
            args.paper, assets, check_only=args.check_paper
        )
        action = "verified" if args.check_paper else (
            "updated" if changed else "already current"
        )
        print(f"paper generated blocks: {action}")
    print(args.out_dir)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
